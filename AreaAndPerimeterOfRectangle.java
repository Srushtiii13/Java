class AreaAndPerimeterOfRectangle 
{
	public static void main(String[] args) 
	{
		float width = 4.5f ;
		float height = 7.9f ;
		float area = width * height;
		float perimeter = 2*(width + height);
		System.out.println("Area of rectangle of width 4.5 and height 7.9 is : " + area);
		System.out.println("Perimeter of rectangle of width 4.5 and height 7.9 is : " + perimeter);
	}
}
